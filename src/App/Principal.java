    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed

        browser.setCurrentDirectory(new File("."));
        browser.setDialogTitle("Seleccione la carpeta para guardar el archivo");
        if(browser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            //File SelectBinder = browser.getCurrentDirectory();
            File SelectBinder = browser.getSelectedFile();
            System.out.print(newfile.IsOlC(SelectBinder.getAbsolutePath()));
            if(newfile.IsOlC(SelectBinder.getAbsolutePath())){
                newfile.NewFile(SelectBinder.getAbsolutePath());
            }else{
                System.out.println("Los siento pana");
            }
        }
    }//GEN-LAST:event_jMenuItem4ActionPerformed

